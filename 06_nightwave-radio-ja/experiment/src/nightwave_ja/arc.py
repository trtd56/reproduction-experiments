def build_host_prompt(kind: str, ctx: Optional[Dict] = None) -> str:
    """素のラジオ局ホスト（セグメント + コーラー）のシステムプロンプト。"""
    # ctx は意図的にシステムプロンプトへ埋め込まない。セグメントごとの事実
    # （曲名/アーティスト、天気、電話の言葉）は USER ターンで渡す
    # （proxy.segment_turn / call_turn / broadcast_turn 参照）。小型モデルが
    # ラベル付き構造をオウム返しするのを防ぐため。ここに ctx を入れないこと。
    instruction = _HOST_KIND.get(kind, _HOST_KIND["thought"])
    return "\n\n".join([HOST_PERSONA, instruction, _output_contract_block()])


