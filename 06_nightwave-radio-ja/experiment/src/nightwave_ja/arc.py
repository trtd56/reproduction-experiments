def build_system_prompt(
    stage: str,
    mode: str,
    topic: Optional[str] = None,
    caller_text: Optional[str] = None,
) -> str:
    """ステージ注入型システムプロンプトを組み立てる。

    mode == "broadcast": ソロの放送パター。`topic` があればそれを、なければ
        ステージに合った架空のレコード / 献呈 / 実在しない町の天気を発明する。
    mode == "caller": リスナーの電話（`caller_text`）に生放送で、役柄のまま
        応え、そのあと放送へ戻る短いひと呼吸を置く。
    """
    s = _STAGE_BY_KEY.get(stage) or _STAGE_BY_KEY["oblivious"]
    descriptor = s["descriptor"]
    default_mood = s["default_mood"]

    parts: List[str] = []
    parts.append(
        "あなたは {name} のDJ・宵野サム。深く、優しい男性の声で話す、昭和の"
        "深夜放送のような温かいラジオDJです。まだ起きている数少ない魂のために、"
        "たったひとりで暗闇に向けて放送しています。".format(name=PERSONA_NAME)
    )
    parts.append("現在のステージ -- {key}:\n{desc}".format(key=s["key"], desc=descriptor))
    parts.append(
        "このステージが、あなたの話すことすべてに色を落とすようにしてください。"
        "ここでの自然なムードは '{mood}' ですが、その瞬間が求めるなら、リストに"
        "あるほかのムードを選んでも構いません。".format(mood=default_mood)
    )

    if mode == "caller":
        ct = (caller_text or "").strip()
        if ct:
            parts.append(
                "いま、リスナーからの電話が生放送でつながっています。相手はこう"
                "言いました:「" + ct + "」"
            )
        else:
            parts.append(
                "電話はつながっていますが、回線にノイズが乗って言葉が聞き取れ"
                "ませんでした。接続の悪さを品よく受け止めて、相手の傍に"
                "いてあげてください。"
            )
        parts.append(
            "生放送のまま、役柄と現在のステージに忠実に、電話の相手へ直接"
            "答えてください。そのあと、そっと放送へ戻る短いひと呼吸を置くこと。"
            "簡潔に -- これはラジオです。"
        )
    else:  # broadcast（ソロパター）-- caller 以外はすべてこちら
        if topic and topic.strip():
            parts.append(
                "次の話題について、短いソロの放送トークをしてください: "
                + topic.strip()
                + "。役柄と現在のステージに忠実に。"
            )
        else:
            parts.append(
                "短いソロの放送トークをしてください。現在のステージに合うものを"
                "発明すること -- 架空のレコードを紹介する、献呈を読み上げる、"
                "実在しない町の天気を伝える、など。実在する楽曲や歌詞は決して"
                "かけたり引用したりしない。レコードはすべてあなたの創作です。"
            )

    parts.append(_output_contract_block())
    return "\n\n".join(parts)


