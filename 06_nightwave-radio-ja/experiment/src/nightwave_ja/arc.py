# key からステージ辞書への高速引き。
_STAGE_BY_KEY = {s["key"]: s for s in STAGES}


def meter_to_stage(meter: int) -> str:
    """0-100 の自覚メーターをステージ key に写像する。

    正式な規則（クライアントと arc.py は厳密に一致すること）:
      oblivious   if meter < 20
      uneasy      if meter < 40
      questioning if meter < 60
      dawning     if meter < 80
      acceptance  otherwise
    """
    m = int(meter)
    if m < 20:
        return "oblivious"
    if m < 40:
        return "uneasy"
    if m < 60:
        return "questioning"
    if m < 80:
        return "dawning"
    return "acceptance"


def stage_default_mood(stage: str) -> str:
    """ステージの既定ムードを返す。未知なら 'warm'。"""
    s = _STAGE_BY_KEY.get(stage)
    if s is None:
        return "warm"
    return s["default_mood"]


